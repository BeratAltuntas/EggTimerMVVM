import Foundation
import UIKit

extension UIFont {
    static func Helvetica(size: CGFloat)-> UIFont {
        return UIFont(name: "Helvetica", size: size)!
    }
    
    static func HelveticaNeue(size: CGFloat)-> UIFont {
        return UIFont(name: "Helvetica Neue", size: size)!
    }
}
